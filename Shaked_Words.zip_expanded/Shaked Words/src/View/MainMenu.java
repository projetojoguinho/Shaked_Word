    public String randomizeWords(Players px){
        //cria a frase randomizada com base na frase enviada por cada player (pega do objeto Players)
        MainMenu m = new MainMenu();
        Random rand = new Random();
        List<Integer> seq = new ArrayList<>();
        String[] fraseOrdenada = px.getFrase().split(" ");
        String fraseReordenada;
        int i;
        
        for(i = 0; i < fraseOrdenada.length; i++){
            seq.add(i);
        }
        
        Collections.shuffle(seq);
        
        fraseReordenada = "<html><p><b>Frase de " + px.getNome() + ":</b></p>\n" + "<p><b>\"";
        
        for(i = 0; i < fraseOrdenada.length; i++){
            fraseReordenada += fraseOrdenada[seq.get(i)] + " ";
        }
        
        fraseReordenada += "\"</b></p></html>";
                
        return fraseReordenada;
        
    }    
    
