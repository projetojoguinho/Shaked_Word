    public void Cronometro(int tipotarefa){
        if(tipotarefa == 1){
            //aqui startar o cronômetro de tempo para criarem as respostas (com base no tempo escolhido no menu NewGame)
            Timer cronometro = new Timer(1000, new Tarefa());
            cronometro.start();
        }else if(tipotarefa == 2){
            //aqui startar o cronômetro para adicionar as respostas de cada player no jogo (cerca de 1 min, dependendo da quantidade)
            Timer cronometro = new Timer(1000, new Tarefa2());
            cronometro.start();     
        }
    }
    
    class Tarefa implements ActionListener{
        @Override
        public void actionPerformed(ActionEvent e) {
            //cronômetro para criarem as respostas (com base no tempo escolhido no menu NewGame)
            if(sec == 00 && min > 00){
                sec = 59;
                min -= 1;
                cronometroJLabel.setText(min + ":" + sec);
            }else if(sec > 0 && min >= 00){
                sec -= 1;
                cronometroJLabel.setText(min + ":" + sec);
            }else{
                cronometroJLabel.setText(min + ":" + sec);
                //aqui vai a ação que para a terefa (dispara uma janela)
            }
        }
    }
    
